// -----------------------
// Fun√ß√£o: editar coment√°rio (s√≥ deve ser chamada por quem √© autor)
// - abre prompt com texto atual, atualiza Firestore e o listener atualiza a UI
// -----------------------
async function editarComentario(docId, textoAtual) {
  try {
    // prompt simples para edi√ß√£o (voc√™ pode trocar por modal customizado)
    const novoTexto = prompt("Edite seu coment√°rio:", textoAtual);
    if (novoTexto === null) return; // usu√°rio cancelou
    const textoLimpo = novoTexto.trim();
    if (textoLimpo.length === 0) { alert("Coment√°rio n√£o pode ficar vazio."); return; }

    // atualiza no Firestore (s√≥ o campo comentario)
    await db.collection("comentarios").doc(docId).update({ comentario: textoLimpo });

    // N√ÉO precisa chamar renderizarDepoimentos(); o onSnapshot atualizar√° automaticamente
    // Podemos opcionalmente mostrar um feedback curto
    feedbackDiv.textContent = "Coment√°rio atualizado com sucesso!";
    feedbackDiv.className = 'text-green-600 font-semibold mt-2';
    setTimeout(() => { feedbackDiv.textContent = ''; feedbackDiv.className = ''; }, 2500);
  } catch (err) {
    console.error("Erro ao editar coment√°rio:", err);
    alert("Erro ao atualizar coment√°rio. Verifique o console.");
  }
}

// -----------------------
// Fun√ß√£o: excluir coment√°rio (s√≥ autor)
// -----------------------
async function excluirComentario(docId) {
  if (!confirm("Deseja realmente excluir seu coment√°rio?")) return;
  try {
    await db.collection("comentarios").doc(docId).delete();
    // onSnapshot ir√° remover o item da UI automaticamente
    feedbackDiv.textContent = "Coment√°rio exclu√≠do com sucesso!";
    feedbackDiv.className = 'text-green-600 font-semibold mt-2';
    setTimeout(() => { feedbackDiv.textContent = ''; feedbackDiv.className = ''; }, 2500);
  } catch (err) {
    console.error("Erro ao excluir coment√°rio:", err);
    alert("Erro ao excluir coment√°rio. Verifique o console.");
  }
}

